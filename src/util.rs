use bootloader::BootInfo;

use crate::kprintln;
use core::arch::asm;
pub unsafe fn hlt_loop() -> ! {
    loop {
        asm!("hlt")
    }
}

pub fn init(boot_info: &'static BootInfo) {
    kprintln!("Beginning initalization");
    kprintln!("Initializing ChadOS::interrupt::gdt::init");
    crate::interrupt::gdt::init();
    kprintln!("Initializing ChadOS::interrupt::idt::init");
    crate::interrupt::idt::init();
    kprintln!("Initializing ChadOS::interrupt::pic::init");
    crate::interrupt::pic::init();
    kprintln!("Initializing ChadOS::mem::paging::init");
    let (mut mapper, mut frame_alloc) = crate::mem::paging::init(boot_info);
    kprintln!("Initializing ChadOS::mem::heap::init");
    crate::mem::heap::init(&mut mapper, &mut frame_alloc).expect("Heap init failed");
    kprintln!("Kernel fully initalized");
}

