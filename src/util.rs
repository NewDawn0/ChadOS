use crate::kprintln;
use bootloader::BootInfo;
use const_format::str_replace;
use core::arch::asm;
pub unsafe fn hlt_loop() -> ! {
    loop {
        asm!("hlt")
    }
}

macro_rules! kinit {
    ($($module:ident)::*) => {{
            kprintln!("Initializing ChadOS::{}::init",str_replace!(stringify!($($module)::*), " :: ", "::"));
            $crate::$($module)::*::init()
        }
    };
    ($($module:ident)::*, $($arg:expr),*) => {{
            kprintln!("Initializing ChadOS::{}::init",str_replace!(stringify!($($module)::*), " :: ", "::"));
            $crate::$($module)::*::init($($arg),*)
        }
    }
}

pub fn init(boot_info: &'static BootInfo) {
    kprintln!("Beginning initalization");
    kinit!(interrupt::gdt);
    kinit!(interrupt::idt);
    kinit!(interrupt::pic);
    let (mut mapper, mut frame_alloc) = kinit!(mem::mem, boot_info);
    kinit!(mem::alloc::init, &mut mapper, &mut frame_alloc).expect("Heap init failed");
    kprintln!("Kernel fully initalized");
}

